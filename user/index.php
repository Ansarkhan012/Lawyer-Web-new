document.addEventListener('DOMContentLoaded', function() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                startCounters();
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });

    const statsSection = document.getElementById('stats-section');
    observer.observe(statsSection);

    function startCounters() {
        const counters = document.querySelectorAll('.counter');
        
        // Different durations for different counters (in milliseconds)
        const durations = {
            "Cases Won": 3000,       // 3 seconds
            "Client Satisfaction": 2500, // 2.5 seconds
            "Expert Attorneys": 2000, // 2 seconds
            "Availability": 1500      // 1.5 seconds
        };

        counters.forEach(counter => {
            const target = +counter.getAttribute('data-target');
            const label = counter.nextElementSibling.innerText;
            const duration = durations[label] || 2000;
            
            animateCounter(counter, target, duration, label);
        });
    }

    function animateCounter(element, target, duration, label) {
        const start = 0;
        const startTime = performance.now();
        
        function updateCounter(currentTime) {
            const elapsedTime = currentTime - startTime;
            const progress = Math.min(elapsedTime / duration, 1);
            const currentValue = Math.floor(progress * target);
            
            // Special formatting during animation
            if (label.includes("Satisfaction") && progress === 1) {
                element.textContent = target + '%';
            } 
            else if (label.includes("Availability") && progress === 1) {
                element.textContent = '24/7';
            }
            else if (label.includes("Cases Won") || label.includes("Expert Attorneys")) {
                element.textContent = currentValue + (progress === 1 ? '+' : '');
            }
            else {
                element.textContent = currentValue;
            }

            if (progress < 1) {
                requestAnimationFrame(updateCounter);
            }
        }
        
        requestAnimationFrame(updateCounter);
    }
});





